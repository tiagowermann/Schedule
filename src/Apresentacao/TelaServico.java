    private void jButtonEditarServicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarServicoActionPerformed
        String idString = String.valueOf(jTableCadastroProdutos.getValueAt(jTableCadastroProdutos.getSelectedRow(), 0));

        id = Integer.parseInt(idString);

        // criar instancia Agenda
        AgendamentoDAO apDAO = new AgendamentoDAO();

        // criar instancia AgendamentoDAO
        Agenda apresentacao = apDAO.consultarServico(id);

        if (apresentacao != null) {
            // define os valores do obj nos campos da tela
            jTextFieldDescricao.setText(apresentacao.getNomeServico());
            jFormattedTextFieldValor.setText(apresentacao.getValorServico());
            jFormattedTextFieldDurcao.setText(apresentacao.getTempoServico());
            //jTextFieldSituacaoServico.setText(String.valueOf(apresentacao.getSituacaoServico()));
            // popula tabela
            apDAO.popularTabelaServico(jTableCadastroProdutos, jTextFieldPesquisarServico.getText());

            // mudar de aba
            //jTabbedPane1.setSelectedIndex(0);
            
            // definir o foco
            jTextFieldDescricao.requestFocus();
        }
    }//GEN-LAST:event_jButtonEditarServicoActionPerformed

    private void jButtonExcluirServicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirServicoActionPerformed
        if (JOptionPane.showConfirmDialog(this, "Deseja realmente excluir ?") == JOptionPane.OK_OPTION) {

            String idString = String.valueOf(jTableCadastroProdutos.getValueAt(jTableCadastroProdutos.getSelectedRow(), 0));

            int idExclusao = Integer.parseInt(idString);
            
            // criar instancia Agenda
            AgendamentoDAO apDAO = new AgendamentoDAO();
            
            // manda excluir
            if (apDAO.excluirServico(idExclusao)) {
                JOptionPane.showMessageDialog(null, "Serviço excluído com sucesso!");
                apDAO.popularTabelaServico(jTableCadastroProdutos, jTextFieldPesquisarServico.getText());
            } else {
                JOptionPane.showMessageDialog(null, "Problemas ao excluir serviço.");
            }
        }
    }//GEN-LAST:event_jButtonExcluirServicoActionPerformed



    private void jButtonCadastrarServicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarServicoActionPerformed

        // criar instancia Agenda
        Agenda apresentacao = new Agenda();

        // criar instancia AgendamentoDAO
        AgendamentoDAO apDAO = new AgendamentoDAO();

        // inicio dos teste de validação
        if (jTextFieldDescricao.getText().length() == 0) {  //Verifica se descrição está vazio.
            System.out.println("Descrição em branco!");
            JOptionPane.showMessageDialog(null, "Descrição em branco!");
        } else {
            if (jFormattedTextFieldValor.getText().length() == 0) { //Verifica se valor esta vazio.
                System.out.println("Valor em branco!");
                JOptionPane.showMessageDialog(null, "Valor em branco!");
            } else {
                if (jFormattedTextFieldDurcao.getText().trim().equals(":")) {    //Verifica se duraçao está vazio.
                    System.out.println("Duração em branco!");
                    JOptionPane.showMessageDialog(null, "Duração em branco!");
                } else {
                    // popular o obj
                    apresentacao.setIdServico(id);
                    apresentacao.setNomeServico(jTextFieldDescricao.getText().toString().toUpperCase());
                    apresentacao.setValorServico(jFormattedTextFieldValor.getText());
                    apresentacao.setTempoServico(jFormattedTextFieldDurcao.getText());
                    //apresentacao.setSituacaoServico(Integer.parseInt(jTextFieldSituacaoServico.getText()));

                    // vou mandar cadastrar
                    if (apDAO.cadastrarServico(apresentacao)) {
                        JOptionPane.showMessageDialog(null, "Serviço salvo com sucesso!");
                        //popula tabela
                        apDAO.popularTabelaServico(jTableCadastroProdutos, jTextFieldPesquisarServico.getText());

                        // limpa os campos
                        jTextFieldDescricao.setText("");
                        jFormattedTextFieldValor.setText("");
                        jFormattedTextFieldDurcao.setText("");
                        //jTextFieldSituacaoServico.setText("");

                        // posiciona cursor
                        jTextFieldDescricao.requestFocus();

                        // resetar o id
                        id = 0;
                    } else {
                        JOptionPane.showMessageDialog(null, "Problemas ao cadastrar serviço");
                    }
                    
                } // FIM else duração em branco.
            } // FIM else valor em branco.
        } // FIM else descricao em branco.


    }//GEN-LAST:event_jButtonCadastrarServicoActionPerformed

